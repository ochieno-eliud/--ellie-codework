            <h1><b>Dairy Farming</b></h1><br><br>
<p>
    Dairy farmers’ production systems worldwide need to be able to combine profitability with the responsibility of protecting
human health, animal health, animal welfare and the environment. Dairy farmers, as the primary producers in the supply chain,
should also be given the opportunity to add value to their product by adopting methods of production that satisfy
the demands of processors and customers. </p><br>
<p>This <i>Guide gives individual dairy farmers proactive guidance</i> on how these objectives can be achieved on their farm.<br>
The Guide to good dairy farming practice has been written in a practical format for dairy farmers engaged in the production of
milk from any dairy species.</p><br>
            <br>
            <h2><b>MISSION STATEMENT</b></h2><br>
  <p>To elaborate a practical, farm orientated, globally achievable Guide to good dairy farming practices for dairy farmers, covering
key aspects of dairy farm management including: animal health, milk hygiene, animal nutrition, animal welfare, the environment
and socioeconomic management.</p><br>
 <br>
            <h2><b>OBJECTIVE AND SCOPE</b></h2><br>
  <p>The guiding objective for good dairy farming practice is that safe, quality milk should be produced from healthy animals using
 management practices that are sustainable from an animal welfare, social, economic and environmental perspective.<br>
To achieve this objective, dairy farmers should apply good practice in the following areas:</p>
  <br>
  <ul>
      <li>animal health;</li><br>
       <li>milking hygiene;</li><br>
        <li>environment;</li><br>
         <li>nutrition (feed and water);</li><br>
          <li>animal welfare; and</li><br>
           <li>ocio-economic management.</li><br>
  </ul>
 <br>
            <h1><b>1. 1. ANIMAL HEALTH</b></h1><br>
            <p>This Fact Sheet describes good dairy farming practice to ensure animals that produce milk are healthy and there is an
effective health care program in place. However, not all of the practices are applicable in all circumstances and may be
superseded by national, international or market demands.<br>
The suggested good dairy farming practices for animal health are set out under the following headings:</p><br>
            <br><ul>
                <li>Establish the herd with resistance to disease.</li><br/>
                <li>Prevent entry of disease onto the farm.</li><br/>
                <li>Have an effective herd health management program in place.</li><br/>
                <li>Use all chemicals and veterinary medicines as directed.</li><br/>
            </ul>
 <br>
            <h1><b>1.2 Prevent entry of disease onto the farm</b></h1><br>
            <p><b><i>1.2.1 Only buy animals of known health status (both herd and individual animals) and control their introduction to<br>
                        the farm using quarantine if indicated</i></b><br></p>
                <p><b><i>1.2.2 Ensure animal transport on and off the farm does not introduce disease</i></b><br></p>
                <p><b><i>1.2.3 Monitor risks from adjoining land and neighbors and have secure
boundaries</i></b><br></p>
               <p> <b><i>1.2.4 Where possible, limit access of people and wildlife to the farm</i></b><br></p>
                <p><b><i>1.2.5 Have a vermin control program in place</i></b><br></p>
                <p><b><i>1.2.6 Only use clean equipment from a known source</i></b><br></p>
 <br>
            <h1><b>1.3 Have an effective herd health management programme in place</b></h1><br>
                <p><b><i>1.3.1 Use an identification system that allows all animals to be identified individually from birth to death</i></b><br></p>
                <p><b><i>1.3.2 Develop an effective farm herd health management programme focused on prevention that meets farm needs<br>
as well as regional and national requirements</i></b><br></p>
                <p><b><i>1.3.3 Regularly check animals for signs of disease</i></b><br>
                <p><b><i>1.3.4 Sick animals should be attended to quickly and in an appropriate way</i></b><br></p>
                <p><b><i>1.3.6 Separate milk from sick animals and animals under treatment</i></b><br></p>
                <p><b><i>1.3.7 Keep written records of all treatments and identify treated animals appropriately</i></b><br></p>
               <p> <b><i>1.3.8 Manage animal diseases that can affect public health (zoonosis)</i></b><br>
