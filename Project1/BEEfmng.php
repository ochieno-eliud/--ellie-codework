            <h2><b>3. MAKING THE TOP BAR</b></h2>
            <br>
            <p>
                Cutting top bars is the most difficult and expensive part of hive building. It is essential they are 32 or 33 mm (3.2 or 3.3 cm) wide.<br>
                The width of the top bars can be measured using a bottle top or a used 9 volt battery (the only square shaped battery).<br>
                Most top bars are wedge shaped in cross section. However, many people have success with simpler top bars made of local<br>
                materials that are easier to cut than best timber. Badly cut top bars are the cause of most problems that can<br>
                happen when using top bar hives. If the top bars are cut correctly and waxed well, bees will build one comb on one top bar.<br>
                This allows the beekeeper to harvest<br>
                easily and move combs between hives to manage bees in a similar way to frame hives.
