        <h1><b> FISH FARMING</b> </h1><br><br>
<p>Fish farms or fish farming is a form of aquaculture. The act of fish farming is about raising fish commercially in tanks or enclosures for human consumption. There are different types of fish farms that utilize different aquiculture methods.
</p><br>
<p>The first method is the cage system which use cages that are placed in lakes, ponds and oceans that contain the fish. This method is also widely referred to as off-shore cultivation. Fish are kept in the cage like structures and are artificially fed and harvested. The fish farming cage method has made numerous technological advances over the years, especially with reducing diseases and environmental concerns. However, the number one concern of the cage method is fish escaping and being loose among the wild fish population.
The second method is irrigation ditch or pond systems for raising fish. This basic requirement for this method is to have a ditch or a pond that holds water. This is a unique system because at a small level, fish are artificially fed and the waste produced from the fish is then used to fertilize farmers fields. On a larger scale, mostly in ponds, the pond is self-sustaining as it grows plants and algae for fish food.
</p><br>

<p>The third method of fish farming is called composite fish culture which is a type of fish farming that allows both local fish species and imported fish species to coexist in the same pond. The number of species depends, but it is sometimes upwards of six fish species in a single pond. The fish species are always carefully chosen to ensure that species can coexist and reduce competition for food.
</p><br>

<p>The fourth method of fish farming is called integrated recycling systems which is considered the largest scale method of  pure fish farming. This approach uses large plastic tanks that are placed inside a greenhouse. There are hydroponic beds that are placed near the plastic tanks. The water in the plastic tanks is circulated to the hydroponic beds, where the fish feed waste goes to provide nutrients to the plant crops that are grown in the hydroponic beds. The majority of types of plants that are grown in the hydroponic beds are herbs such as parsley and basil.
</p> <br><br>

<h2><i>Catfish farming<i></h2><br><br>
<p>Catfish are easy to farm in warmer climates. Catfish are predominantly farmed in fresh water ponds and fed mostly soybeans, corn and rice. Catfish are often considered one of the more sustainable fish specifies for fish farming purposes. Catfish is populous because of its health benefits and market demand. Farm-raised catfish are usually harvested at 18 months of age where as wild catfish usually get much bigger. There are a number of catfish species, but the three most prominent ones are blue catfish, channel catfish, and flathead catfish.
</p><br><br>
<h2><i>Tilapia farming<i></h2><br><br>
<p>Tilapia is the third most popular fish used in fish farming or aquaculture, with the first two being carp and salmon. They have increased in popularity due to their high protein, large size and growth capabilities. Tilapia is a tropical fish that requires warmer water to survive. The ideal water temperature is usually between 28 to 30 degrees C. Tilapia fish are known to reproduce rapidly and this is a challenge for managing tilapia fish species for farming use. If not managed properly, fish will aggressively compete for food which may result in stunted growth. Therefore, males are almost used exclusively. Tilapias are resilient towards fighting off diseases and parasites. Tilapia fish farming originated in Africa and is popular in Honduras, Papua New Guinea, Philippines and Indonesia. Tilapia fish require a cereal-based diet and don’t eat other fish, but they are also considered to be one of the most invasive fish species.
</p><br><br>
<h2>Salmon farming</h2><br><br>
<p>Salmon is one of the most popular fish species with the most commonly farmed being Atlantic salmon. There are two other varieties of Pacific salmon that are also farmed Chinook and Coho. Farmed salmon are vaccinated to prevent disease outbreaks and only on rare occasions do they require additional medications. There are often questions about the different colors between wild and farmed salmon farmed salmon arent dyed their color comes from their food. Salmon feed is made to conserve wild fish stocks.
</p><br><br>
<h2>Tuna farming</h2><br><br>
<p>Tuna fish are saltwater fish and are important in the commercial fish farming industry. Japan is the biggest consumer of tuna and has invested a significant amount of research into studying the fish. There are different species of tuna including, Bluefin, yellowfin, and albacore. Bluefin tuna populations have dropped significantly in some regions due to over fishing. Farming tuna is complex as the fish are massive and are very active so simulating their natural environment is extremely difficult. Most tuna for human consumption are caught in the wild and raised in a facility to increase weight gain. Tuna are carnivorous and eat other fish. Tuna are typically farmed in net pens offshore and in some cases are farmed in recirculation systems.
</p><br><br>
<h2><b><i>Fish farming supplies</i></b></h2><br><br>
<p>Fish farming supplies are needed for the aquaculture industry. The supplies can range from feed and feeders, filtration systems, hatchery supplies, heating and cooling systems, lighting, hydroponics equipment, pluming, predator control, tanks and water treatment products just to name a few.<br>
<p>
Feed and feeders Feed quality is important in fish farming and is vital to fish health. Depending on the feed that is given, it can help with desirable color, growth and overall health and well-being. There are a wide variety of different feeds that are suited to different types of aqua-farming methods and species.</p><br>
<p>
Water Filtration Systems Water filtration systems are important when attempting to mitigate environmental impacts. Filtration involves the removal of waste products from the water. There are a number of different types filtering systems that can be used, but it often depends on the state of the filtration process. It is important to have a staging filtration system that will ensure most optimal results.</p><br>
<p>
Hatchery supplies Hatchery supplies include anything from fish graders, shipping supplies to spawning and handling containers.</p>
<p><br>
Predator control Predator control tools are used in the fish habitat, which often includes the use of physical deterrents which may include visual and audio deterrents.
</p><br><br>





            
