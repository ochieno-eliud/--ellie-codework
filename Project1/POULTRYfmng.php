        <h1><b><i>POULTRY FARMING</i></b></h1><br><br>
<p>Poultry farming means raising various types of domestic birds commercially for the purpose of meat, eggs and feather production. The most common and widely raised poultry birds are chicken. About 5k million chickens are being raised every year as a source of food (both meat and eggs of chicken). The chickens which are raised for eggs are called layer chicken, and the chickens which are raised for their meat production are called broiler chickens. <br> However, in a word commercial poultry farming is very necessary to meet up the demand of animal nutrition (eggs and meat). Commercial poultry farming is also very profitable. And commercial poultry farming business is one of the traditional business ventures. Here we are describing more about the advantages of poultry farming business and the steps for running this business.
Various Methods of Poultry Farming</p><br><br>
<h2><i>Layer Poultry Farming</i></h2><br><br>
<p>The poultry birds which are raised for egg production are called layer poultry. Commercial hen generally starts laying eggs at the age of 12-20 weeks. They start laying eggs regularly at their 25 weeks of age. After 70-72 weeks of age egg production of layer poultry get reduced. For commercial layer poultry farming, producers generally keep the hens for 12 months from their first laying period. And then sell them for slaughter purpose. Although chickens naturally survive for more than 6 years. For re-invigorating egg laying, the hens are force moulted in some countries.<br> For commercial egg laying poultry farming systems, the environmental conditions are often automatically controlled by the producers. For a simple example, presence of light helps the bird for laying eggs earlier. So, the producers should provide more lightening period to increase the probability of beginning laying eggs. The egg-laying birds lays more eggs in warmer months than the cold months. So, keeping the temperature of the room moderate will be very helpful for better egg production. Some commercial egg laying chicken breeds can produce more than 300 eggs a year. Layer poultry are raised in various methods.
</p><br><br>
<ul><h3>Free Range Farming</h3><br><br>
<p>Free range poultry farming means providing freely roaming facilities to the poultry birds for a certain period of a day. Although they are kept inside the house at night to keep them free from predators and adverse weather. In free range farming method the poultry birds generally roam freely throughout the whole day. Which means they spent half of their life outside the house. For free range poultry farming system select a suitable land which has the facilities of adequate drainage system, good ventilation, appropriate protection from prevailing winds, good protection from all types of predators and free from excessive cold, heat or dampness. Excessive cold, heat and damp is very harmful for poultry birds and reduce their productivity. This system also requires less feed than cage and barn systems. The poultry manure from free range farming used as fertilizer for crops directly. Although free range farming method is very suitable for poultry birds but it has some difficulties too. In this system the poultry birds can be victim of predators easily and may caught by various type of diseases.
</p><br><br>
<h3>Organic Method</h3><br><br>
<p>Organic layer poultry rearing system is also one type of free range farming system. But the main differences between the two systems are, in free range farming method a large numbers of poultry birds are raised together but in organic method a certain species of poultry bird are raised in small group with low stocking density. Organic laying system has some restrictions in the routine use of synthetic yolk colourants, water, feed, medications, other feed additives and obviously a smaller group size with low stocking density. In organic laying system the producer should keep highest 1000 poultry birds per hector and maximum 2000 birds in each house
</p><br><br>
<h3>Yarding Method</h3><br><br>
<p>Yarding poultry farming method is such a method in which cows and chickens are raised together. The farmer make a fence in his yard and keep all the poultry birds and cattle there together. The birds and cattle has the freedom of movement inside the fence. It is a very popular system used by small farmer
</p><br><br>
<h3>Battery Cage Method</h3><br><br>
<p> Battery cage layer poultry rearing method is one of the very common methods used in many countries. In this system usually small sized metal cages are used. Every cages can accommodate about 3 to 8 hens. The walls of the cages are generally made of mesh or solid metal and the floor is made of sloped wire mesh which allow the faeces to drop down. When the hens lays eggs, then all the eggs gather in the egg collecting conveyor belt of the cage. <br>In this system food is provided in front of the hens by a long bisected metal or plastic pipe and water served to them by using overhead nipple systems. The cages are arranged in long rows in one above another system. There may have several floors in a single shade which can keep many even thousands of hens together. For reducing feather and vent pecking, the light intensity is generally kept lower than 10 lux. The battery cage method has some benefits. The main benefits of battery cage are listed below.
</p><br>
<li>It is very easy to care for the birds.</li><br>
<li>Very easy to collect eggs.</li><br>
<li>Cleaner eggs.</li><br>
<li>Requires less feed to produce eggs.</li><br>
<li>Thousands of hens may be housed in a specific floor space of the house.</li><br>
<li>The birds suffers less by internal parasites.</li><br>
<li>Labor cost is very low.</li></ul><br><br>
<p>Besides those benefits battery cage system has some difficulties too. By rearing large number of hens in a small place the air inside the house may contain high ratio of co2. The hens cant get sufficient space to walk, flap their wings, stand or perch. For this reason they may suffer by frustration and boredom and their behaviors may change which affect their production. Battery cage system is banned in some countries because it is considerate as against the animal welfare.
</p><br><br>
<h2><i>Furnished Cage Method</i></h2><br><br>
<p> Furnished cage method is a developed version of battery cage system. In this system the hens get more spaces and facilities than battery cage system. A furnished cage for hens should contain sufficient space for walk, perch, flap their wings, nest, special feed and water pot etc.
</p><br><br>
<h2><i>Broiler Poultry Farming</i></h2><br><br>
<p>The poultry birds which are raised for commercial meat production are called broiler poultry. By using modern farming methods broiler chickens become suitable for consumption within their 5 to 6 weeks of age. However, see the common raising systems which are mostly used for commercial broiler poultry farming.<br>
</p>
<p>Indoor Raising Methods: In this method broilers are kept inside a house. Rice hulls, wood shavings, peanut shells etc. are used as litter in the floor of the house. In this system the broilers are kept in a large and open house (known as grow out houses) and they become suitable for consumption within their 5 to 6 weeks of age.  This types of poultry houses are well equipped with mechanical systems for delivering the feed and water to the poultry birds. Well ventilation system, coolers and heaters are must. It is very important to keep the house always dry and clean. Generally a house of 400 feet long and 40 feet wide can accommodate about 20,000 birds. One-half square feet space is required per bird.<br>
</p>
<p>Free-Range Methods: In free-range broiler farming methods the broilers are kept like the free-range layers. The broiler breeds which grows slowly (takes more than 8 weeks for reaching slaughter weight) are suitable for raising in this systems. The main facilities of free-range farming systems is that, it allows the birds scratching, foraging, pecking and outdoor exercise.
</p><br>
<p>Organic Farming Methods: Organic farming method is almost the same as free-range farming systems. But the main difference is that, in organic farming methods the birds are not allowed for randomly using of in-feed or in-water medications, other food additives and synthetic amino acids. This system is very suitable for the poultry breeds which reach slaughter weight slowly (around 12 weeks).
</p><br><br>
<h2><i>Poultry Housing</i></h2><br><br>
<p>Good and suitable housing play a vital role in raising all types of poultry birds. Some birds grow and live happily in the floor of poultry house and some in cages. Depending on the birds, you have to make a suitable house for your birds and ensure availability of all types of necessary facilities for them. Consider the following aspects while building houses for your poultry birds.
</p><br><br>
<ul><li>Always keep sufficient space in your poultry house, depending on the birds. So that your birds can live, grow and produce happily. Never overcrowded the poultry house.
</li><br>
<li>Good ventilation system is a must. Ensure that you poultry houses are well ventilated.</li>
<li>You also have to ensure sufficient flow of fresh air and light inside the house.</li>
<li>If you go for commercial production, keep proper distance between one house to another house.</li>
<li>Clean the house and equipment on a regular basis. Sterilize the house before bringing new chicks into your farm.</li><br>
<li>Take necessary steps for preventing all types of predators and harmful animals.</li>
<li>Make good temperature management so that you birds do not suffer by excessive hot or cold.</li>
<li>Make suitable drainage system inside the house for cleaning it properly.</li>
<li>Making poultry houses in calm and quite place is always a good idea. See our housing guides for various types of poultry birds.</li></ul><br><br>
<h2><i>Feeding</i></h2><br><br>
<p>feeding high quality, fresh and nutritious food always ensure good health, proper growth and high production. So, always feed your poultry birds healthy and nutritious feeds. Add all types of necessary vitamins and minerals to their feed. Commercial poultry feeds for various types of birds are available in the market. You can easily feed this to your birds. Along with feeding your birds high quality and nutritious feeds, always serve them sufficient amount of fresh and clean water according to their demand.
Care & Management
always take good care of your birds. Learn more about various types of poultry diseases, symptoms and treatment. Vaccinate the birds timely. Provide them nutritious feed and clean water. Clean their house on a regular basis.
Marketing
Selling process of various types of poultry products are very easy. Poultry products have a huge demand globally. So you dont have to worry about marketing your products. You can easily sell your farm products in your nearest local market or big supermarkets.
</p><br><br>
            
