            <h1><u>If you plan to go into cage farming, what you need to know</u></h1>
            <br><br>
            <p>In October last year, fish farmers at Nyenye Beach in Siaya County suffered massive losses after strong
                currents ripped <br>through 128 cages killing more than 200, 000 fish.</p><br>
            <p>Although Kenya Marine Fisheries and Research Institute (Kemfri) experts are yet to table a report on 
                what exactly happened, local experts told the farmers that it may have been due to strong 
                currents that hit the unsheltered shores. Prof Stephen Agong&apos; of Jaramogi Oginga 
                Odinga University attributed the catastrophe to lack of oxygen, citing a 
                complex phenomenon called upwelling - where oxygen-poor water from 
                the bottom of the lake mix with that of the surface, 
                decreasing the oxygenation of the surface water.              
            </p><br><p>And as the farmers grapple with the losses and wait for new direction, Kenneth Omondi, an
                aquaculture expert who has 200 in the lake says to avoid such disasters in future, farmers 
                have to be in the know before they venture into cage fish farming. He says lack of 
                knowledge is partly to blame for losses incurred when fish die, or fail to 
                grow to full size. <br>Not every part of the lake is ideal for farming, he explains.</p><br>
            <p>The perfect location for a cage, he says, should be a sheltered beach, like a lagoon, 
                where winds are mild and fish are not &quot;stressed by strong currents”.</p><br>
            <p>Such beaches, he says, are also sheltered from invasion by hyacinth which chokes 
                oxygen supply, killing fish or slowing their growth.</p><br>
